
def get_min_max(input_array):
    """Return a tuple containing the min, max values of the unsorted input array"""
    if len(input_array) < 1:
        return None, None
    max = input_array[0]
    min = input_array[0]

    for value in input_array:
        max = value if value > max else max
        min = value if value < min else min

    return min, max


